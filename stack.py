#Declaring an empty stack
stack = []

#Adding elements to the stack
stack.append("mango")
stack.append("kiwi")
stack.append("orange")
stack.append("pineapple")
stack.append("watermelon")
print(stack)

#Removing elements in a stack
print(stack.pop(2))
print(stack)


#Accessing an element in a stack
print(stack[0])
print(stack[3])